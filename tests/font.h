int atlas_chardata(int codepoint, atlasrect *box, unsigned char *offset) {
    if(32 <= codepoint && codepoint < 127) {
        codepoint -= 32;
        *box = boxes[codepoint];
        *offset = offsets[codepoint];
        return 1;
    }
    switch(codepoint) {
        case 0xf00c: *box = boxes[95]; *offset = offsets[95]; return 1;
        case 0x0000: *box = boxes[96]; *offset = offsets[96]; return 1;
    }
    return 0;
}
